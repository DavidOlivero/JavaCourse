
public class GuardarDatosVariables {

	public static void main(String[] args) {
		
		/* Tipos de datos en Java
		 *Enteros:
		 *int -> 4 bytes de almacenamiento
		 *short -> 2 bytes de almacenamiento
		 *long -> 8 bytes de alamacenamiento se usa el sufijo L al final del número
		 *byte -> 1 byte de almacenamiento
		 *Coma flotante (decimales):
		 *float -> 4 bytes se usa el sufijo F al final del número
		 *double -> 8 bytes
		 *Carácteres:
		 *char -> deben de ir entre comillas
		 *Verdadero o Falso:
		 *boolean -> Datos true y false
		*/
		
		// Ejemplo 1:
		
		byte edad1; //Declaración de la variable
		edad1 = 32; //Iniciación de la variable
		System.out.println(edad1); //Imprimir contenido de la variable
		
		// Ejemplo 2:
		
		byte edad2 = 75; //Declaración e inicialización de la variable
		System.out.println(edad2); //Imprimir contenido de la variable

	}

}
